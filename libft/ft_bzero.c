#include<stddef.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*dest;

	if (!s)
		return ;
	dest = (char *)s;
	i = 0;
	while (i < n)
	{
		*(dest + i) = (char)0;
		i++;
	}
}
